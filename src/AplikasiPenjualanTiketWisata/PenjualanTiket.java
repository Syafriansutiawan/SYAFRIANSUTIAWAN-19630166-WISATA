    private void tampilkan() throws SQLException{
        int row = tLaporanPesanan.getRowCount();
        for (int a=0;a<row;a++)
            model.removeRow(0);
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/19630166_wisata", "root", "");
            ResultSet rs = con.createStatement().executeQuery("selecr * FROM tbl_pemesanan_tiket");
            while (rs.next()){
            String data[] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)};
            model.addRow(data);
            }
        }catch (SQLException ex){
          Logger.getLogger(PenjualanTiket.class.getName()).log(Level.SEVERE, null, ex);
        }
                       
    }
