    private void tLaporanPesananMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tLaporanPesananMouseClicked
        int i = tLaporanPesanan.getSelectedRow();
        if (i >-1){
            namalengkap.setText(model.getValueAt(i,0).toString());
            nomoridentitas.setText(model.getValueAt(i,1).toString());
            tanggalpemesanan.setText(model.getValueAt(i,2).toString());
            txthasilharga.setText(model.getValueAt(i,3).toString());
            cbjbanyaktiket.setSelectedItem(model.getValueAt(i,4).toString());
            
          
        }
    }//GEN-LAST:event_tLaporanPesananMouseClicked

